/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  HAL_Init();
  SystemClock_Config();

  MX_GPIO_Init();
  MX_USART2_UART_Init();

  HAL_UART_Receive_IT(&huart2, &rx_data, 1);

  while (1)
  {
    if (blink_sequence_flag)
    {
      for (int pin = 12; pin <= 15; pin++)
      {
        HAL_GPIO_WritePin(GPIOD, (1 << pin), GPIO_PIN_SET);
        HAL_Delay(1000);
        HAL_GPIO_WritePin(GPIOD, (1 << pin), GPIO_PIN_RESET);
      }
      blink_sequence_flag = 0;
    }
  }
}

